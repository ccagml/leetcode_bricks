// @lc code=start
class Solution
{
public:
    int combinationSum4(vector<int> &nums, int target)
    {
        vector<unsigned int> vi(target + 1);
        vi[0] = 1;
        for (int pri = 1; pri <= target; pri++)
        {
            for (int c : nums)
            {
                if (pri >= c)
                {
                    vi[pri] += vi[pri - c];
                }
            }
        }
        return vi[target];
    }
};
